  CREATE OR REPLACE EDITIONABLE FUNCTION "MVCHALLENGE"."FN_VALIDA_SENHA" (p_senha IN VARCHAR2)
RETURN NUMBER
AS
  e_nula  EXCEPTION;
  e_curta EXCEPTION;
  e_fraca EXCEPTION;
BEGIN
  IF p_senha IS NULL            THEN RAISE e_nula;  END IF;
  IF LENGTH(p_senha) < 8        THEN RAISE e_curta; END IF;
  IF NOT REGEXP_LIKE(p_senha,'[A-Z]')
     OR NOT REGEXP_LIKE(p_senha,'[a-z]')
     OR NOT REGEXP_LIKE(p_senha,'[0-9]')
     OR NOT REGEXP_LIKE(p_senha,'[^A-Za-z0-9]') THEN
    RAISE e_fraca;
  END IF;

  RETURN 1;

EXCEPTION
  WHEN e_nula  THEN RAISE_APPLICATION_ERROR(-20100,'Senha nula');       RETURN 0;
  WHEN e_curta THEN RAISE_APPLICATION_ERROR(-20101,'Senha muito curta'); RETURN 0;
  WHEN e_fraca THEN RAISE_APPLICATION_ERROR(-20102,'Senha fraca');       RETURN 0;
  WHEN OTHERS  THEN RAISE_APPLICATION_ERROR(-20103,'Erro em FN_VALIDA_SENHA: '||SQLERRM); RETURN 0;
END;

