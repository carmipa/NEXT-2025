  CREATE OR REPLACE EDITIONABLE PROCEDURE "MVCHALLENGE"."PRC_SOMAR_SALDOS_AGENCIA_CONTA" 
AS
  CURSOR cur IS
    SELECT AGENCIA, CONTA, SALDO
    FROM TB_SALDO_FATO
    ORDER BY AGENCIA, CONTA;

  v_ag_atual TB_SALDO_FATO.AGENCIA%TYPE := NULL;
  v_subtotal NUMBER(14,2) := 0;
  v_total    NUMBER(14,2) := 0;

  e_sem_dados EXCEPTION;
  e_overflow  EXCEPTION;
  PRAGMA EXCEPTION_INIT(e_overflow, -01426);

  v_seen BOOLEAN := FALSE;
BEGIN
  DBMS_OUTPUT.PUT_LINE('Agencia  Conta   Saldo');
  DBMS_OUTPUT.PUT_LINE('-------  -----   -------');

  FOR r IN cur LOOP
    v_seen := TRUE;

    IF v_ag_atual IS NULL THEN
      v_ag_atual := r.AGENCIA;
    ELSIF r.AGENCIA <> v_ag_atual THEN
      DBMS_OUTPUT.PUT_LINE('Sub Total          '||TO_CHAR(v_subtotal,'99999990D99'));
      v_total    := v_total + v_subtotal;
      v_subtotal := 0;
      v_ag_atual := r.AGENCIA;
    END IF;

    v_subtotal := v_subtotal + r.SALDO;

    DBMS_OUTPUT.PUT_LINE(
      LPAD(r.AGENCIA,7)||'  '||
      LPAD(r.CONTA,5)||'   '||
      LPAD(TO_CHAR(r.SALDO,'99999990D99'),9)
    );
  END LOOP;

  IF NOT v_seen THEN RAISE e_sem_dados; END IF;

  DBMS_OUTPUT.PUT_LINE('Sub Total          '||TO_CHAR(v_subtotal,'99999990D99'));
  v_total := v_total + v_subtotal;
  DBMS_OUTPUT.PUT_LINE('Total Geral        '||TO_CHAR(v_total,'99999990D99'));

EXCEPTION
  WHEN e_sem_dados THEN
    DBMS_OUTPUT.PUT_LINE('Sem dados em TB_SALDO_FATO.');
  WHEN e_overflow THEN
    DBMS_OUTPUT.PUT_LINE('Overflow num√©rico ao somar saldos.');
  WHEN ZERO_DIVIDE THEN
    DBMS_OUTPUT.PUT_LINE('Divis√£o por zero (n√£o esperado neste proc).');
  WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE('Erro em PRC_SOMAR_SALDOS_AGENCIA_CONTA: '||SQLERRM);
END;

