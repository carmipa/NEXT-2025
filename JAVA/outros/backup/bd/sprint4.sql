  CREATE OR REPLACE EDITIONABLE FUNCTION "MVCHALLENGE"."FN_CLIENTE_VEICULOS_JSON" (p_id_cliente IN NUMBER)
RETURN CLOB
AS
  e_cliente_nao_encontrado EXCEPTION;
  PRAGMA EXCEPTION_INIT(e_cliente_nao_encontrado, -20001);

  v_json   CLOB := '';
  v_nome   TB_CLIENTE.NOME%TYPE;
  v_sobre  TB_CLIENTE.SOBRENOME%TYPE;
  v_cpf    TB_CLIENTE.CPF%TYPE;
BEGIN
  SELECT NOME, SOBRENOME, CPF
    INTO v_nome, v_sobre, v_cpf
    FROM TB_CLIENTE
   WHERE ID_CLIENTE = p_id_cliente;

  v_json := '{' ||
            '"idCliente":'||p_id_cliente||','||
            '"nomeCompleto":"' || REPLACE(v_nome||' '||v_sobre,'"','\"') || '",'||
            '"cpf":"' || v_cpf || '",'||
            '"veiculos":[';

  DECLARE
    v_first BOOLEAN := TRUE;
  BEGIN
    FOR r IN (
      SELECT v.ID_VEICULO, v.PLACA, v.MODELO, v.FABRICANTE, v.ANO
        FROM TB_CLIENTEVEICULO cv
        JOIN TB_VEICULO v ON v.ID_VEICULO = cv.TB_VEICULO_ID_VEICULO
       WHERE cv.TB_CLIENTE_ID_CLIENTE = p_id_cliente
       ORDER BY v.ID_VEICULO
    ) LOOP
      IF NOT v_first THEN v_json := v_json || ','; END IF;
      v_first := FALSE;

      v_json := v_json ||
        '{'||
          '"idVeiculo":'||r.ID_VEICULO||','||
          '"placa":"' || r.PLACA || '",'||
          '"modelo":"' || REPLACE(r.MODELO,'"','\"') || '",'||
          '"fabricante":"' || REPLACE(r.FABRICANTE,'"','\"') || '",'||
          '"ano":'||r.ANO||
        '}';
    END LOOP;
  END;

  v_json := v_json || ']}';
  RETURN v_json;

EXCEPTION
  WHEN NO_DATA_FOUND THEN
    RAISE_APPLICATION_ERROR(-20001,'Cliente n√£o encontrado para ID='||p_id_cliente);
  WHEN VALUE_ERROR THEN
    RAISE_APPLICATION_ERROR(-20002,'Erro de tipo/concatena√ß√£o ao montar JSON');
  WHEN OTHERS THEN
    RAISE_APPLICATION_ERROR(-20003,'Falha inesperada em FN_CLIENTE_VEICULOS_JSON: '||SQLERRM);
END;

